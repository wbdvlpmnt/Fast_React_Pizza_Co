function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Company</h1>;
    </header>
  );
}

function Menu() {
  const pizzas = pizzaData;
  return (
    <main className="menu">
      <h2>Our Menu</h2>
      {pizzas.length > 0 ? (
        <React.Fragment>
          <p>Authentic pizzas with fresh ingredients</p>

          <ul className="pizzas">
            {pizzas.map((pizza) => {
              return <Pizza p={pizza} key={pizza.name} />;
            })}
          </ul>
        </React.Fragment>
      ) : null}
    </main>
  );
}

function Pizza({ p }) {
  return (
    <li className="pizza">
      <img src={p.photoName} alt={p.name} />
      <div>
        <h3>{p.name} </h3>
        <p>{p.ingredients} </p>
        <span>{p.soldOut ? "SOLD OUT" : p.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 8;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} openHour={openHour} />
      ) : (
        <p>
          We're happy to serve you between {openHour}:00 and {closeHour}:00
        </p>
      )}
    </footer>
  );
}

function Order({ closeHour, openHour }) {
  return (
    <div className="order">
      <p>
        We're open from {openHour}:00 until {closeHour}:00. Come visit us or
        order online.
      </p>
      <button className="btn">Order</button>
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));

root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);
