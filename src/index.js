function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1>Fast React Pizza Company</h1>;
    </header>
  );
}

function Menu() {
  return (
    <main className="menu">
      <h2>Our Menu</h2>
      <ul className="pizzas">
        {pizzaData.map((pizza) => {
          return <Pizza p={pizza} key={pizza.name} />;
        })}
      </ul>
    </main>
  );
}

function Pizza(props) {
  return (
    <li className="pizza">
      <img src={props.p.photoName} alt={props.p.name} />
      <div>
        <h3>{props.p.name} </h3>
        <p>{props.p.ingredients} </p>
        <span>{props.p.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 8;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;
  console.log(isOpen);
  // What it looks like without JSX
  //return React.createElement("footer", null, "We're currently open!");
  return <footer className="footer">{isOpen && <p>Open</p>}</footer>;
}

